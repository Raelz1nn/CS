package src;

public class ex2 {
    /* Faça um programa que mostre na tela todos os números inteiros de 55 a 118. */
    public static void main(String[] args) {
        // Utilizando um loop for para iterar de 55 a 118
        for (int i = 55; i <= 118; i++) {
            System.out.println(i); // Imprime o número atual do loop
        }
    }
}
