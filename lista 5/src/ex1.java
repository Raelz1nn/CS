package src;

public class ex1 {
    /* Faça um programa que mostre na tela todos os números inteiros de 1 a 100 . */
    public static void main(String[] args) {
        int numero = 1; // Inicializa a variável número com 1

        while (numero <= 100) { // Enquanto número for menor ou igual a 100
            System.out.println(numero); // Imprime o número atual
            numero++; // Incrementa o número para a próxima iteração
        }
    }
}
